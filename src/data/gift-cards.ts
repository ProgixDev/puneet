const buildCards = (
  batchId: string,
  facilityId: number,
  start: number,
  count: number,
  soldCount: number,
): PhysicalCard[] =>
  Array.from({ length: count }, (_, i) => {
    const num = String(start + i).padStart(4, "0");
    const barcode = `BC${facilityId}${num}${String(Math.floor(Math.random() * 9000) + 1000)}`;
    const isSold = i < soldCount;
    return {
      id: `pc-${batchId}-${num}`,
      batchId,
      facilityId,
      cardNumber: `PHYS-${num}-${String(start + i + 5000).slice(-4)}-${String.fromCharCode(65 + (i % 26))}`,
      barcode,
      status: isSold ? "sold" : "inactive",
      // Match the 3-digit zero-padded gift card ids (gc-001..gc-012).
      giftCardId: isSold ? `gc-${String(i + 1).padStart(3, "0")}` : undefined,
      soldAt: isSold ? "2024-02-14T10:00:00Z" : undefined,
      soldByStaff: isSold ? "Sarah Johnson" : undefined,
      soldByStaffId: isSold ? 1 : undefined,
    };
  });

