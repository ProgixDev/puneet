export function getLoyaltyAccountsByFacility(
  facilityId: number,
): CustomerLoyaltyAccount[] {
  return loyaltyAccounts.filter((a) => a.facilityId === facilityId);
}

export function getLoyaltyAccount(
  facilityId: number,
  customerId: number,
): CustomerLoyaltyAccount | undefined {
  return loyaltyAccounts.find(
    (a) => a.facilityId === facilityId && a.customerId === customerId,
  );
}
